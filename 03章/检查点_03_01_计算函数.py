# 函数的运算
import math

print(math.sqrt(4) )  # 2
print(math.sin(2 * math.pi))  # 2
print(math.cos(2 * math.pi))  # 2
print(min(2, 2, 1))  # 1
print(math.log(math.e))  # 1
print(math.exp(1))  # 2.71828
print(max(2, 3, 4))  # 4
print(abs(-2.5))  # 2.5
print(math.ceil(-2.5))  # -2
print(math.floor(-2.5))  # -3
print(round(3.5))  # 4
print(round(-2.5))  # -2
print(math.fabs(2.5))  # 2.5
print(math.ceil(2.5))  # 3
print(math.floor(2.5))  # 2
print(round(-2.5))  # -2
print(round(2.6))  # 3
print(round(math.fabs(-2.5)))  # 2



