# 三角形面积
x1, y1 = eval(input("请输入三角形一个点的坐标，中间以英文逗号分隔：") or "1.5, -3.4")
x2, y2 = eval(input("请输入三角形一个点的坐标，中间以英文逗号分隔：") or "4.6, 5")
x3, y3 = eval(input("请输入三角形一个点的坐标，中间以英文逗号分隔：") or "9.5, -3.4")

side1 = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
side2 = ((x3 - x1) ** 2 + (y3 - y1) ** 2) ** 0.5
side3 = ((x3 - x2) ** 2 + (y3 - y2) ** 2) ** 0.5

s = (side1 + side2 + side3) / 2
area = (s * (s - side1) * (s - side2) * (s - side3)) ** 0.5

print("三角形的面积是：", area)
